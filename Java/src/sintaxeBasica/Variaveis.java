package sintaxeBasica;

public class Variaveis {

	public static void main(String[] args) {
		//Variável é um nome dado a um espaço na memória que armazena um valor.
		//Cada variável tem um tipo de dado que define que tipo de valor pode ser armazenado
		
		String name = "Isaac";
		int ide = 23;
		Boolean eleeLindo = true;
		double id1 = 30;
		
		System.out.println(eleeLindo);
		System.out.println(id1);
		System.out.println(name);
		System.out.println(ide);
	}

}
